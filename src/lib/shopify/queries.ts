/**
 * Create a checkout URL from a cart.
 *
 * Note: In the 2024-01 Storefront API, `cart.checkoutUrl` is returned
 * directly from cart queries/mutations. This standalone query lets you
 * fetch just the URL when you already have a cart ID.
 */
export const CREATE_CHECKOUT_MUTATION = `
  query GetCheckoutUrl($cartId: ID!) {
    cart(id: $cartId) {
      id
      checkoutUrl
    }
  }
`;
